#Sintaxis de operadores aritmeticos
numero1 = int(input("Ingrese un número: "))
numero2 = int(input("Ingrese un número: "))

#Imprimir resultados
print(f'la suma de los numero es: {numero1 + numero2} ')
print(f'la resta de los numero es: {numero1 - numero2} ')
print(f'la division de los numero es: {numero1 / numero2} ')
print(f'la modulo de los numero es: {numero1 % numero2} ')
